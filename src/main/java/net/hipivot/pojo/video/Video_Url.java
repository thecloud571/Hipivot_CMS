	private String horizontalUrl;
	private String verticalUrl;
	private String defaultPlayUrl;
	private String mediumPlayUrl;
	private String highPlayUrl;
	private String defaultDownloadUrl;
	private String mediumDownloadUrl;
	private String highDownloadUrl;

